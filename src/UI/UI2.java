    private void mostrarTodos() {
        this.routes = new ArrayList<>();
        for (int i = 0; i < this.airports.airports.size(); i++) {
            ArrayList<Nodo> route = new ArrayList<Nodo>();
            route.add(this.airports.airports.get(i));
            routes.add(route);
        }
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (Nodo airport: airports.airports)
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, "(" + airport.lon + ", " + airport.lat + ")", 0});
        Map.updateUI();
    }

    public class Mapa extends javax.swing.JPanel {
        
         private JXMapKit mapKit;
        
        @Override
        public void paint (Graphics g) {
            System.out.println("Dibujado");
            
            BufferedImage image;
            try {
                image = ImageIO.read(getClass().getResource("/data/Mapa.jpg"));
                ImageIcon imgTab = new ImageIcon (new ImageIcon (image).getImage().getScaledInstance(Map.getWidth(), Map.getHeight(), java.awt.Image.SCALE_SMOOTH));
                g.drawImage(imgTab.getImage(), 0, 0, rootPane); //Size 700, 700
                
                Nodo prev = null;
                for (ArrayList<Nodo> route: routes) {
                    for (Nodo airport: route) {
                        if (prev == null) {
                            drawAirport(g, airport);
                        } else {
                            drawAirport(g, airport);
                            drawRoute(g, prev, airport);
                        }
                        prev = airport;
                    }
                    prev = null;
                }

                
            } catch (Exception e) {
                ImageIcon imgTab = new ImageIcon (new ImageIcon (getClass().getResource("/data/Mapa.jpg")).getImage().getScaledInstance(Map.getWidth(), Map.getHeight(), java.awt.Image.SCALE_SMOOTH));
                g.drawImage(imgTab.getImage(), 0, 0, rootPane); //Size 700, 700
                Logger.getLogger(UI2.class.getName()).log(Level.SEVERE, null, e);
            }
        }
        
        public void drawRoute (Graphics graphics, Nodo source, Nodo destination) {   
            int bubble_size = 10;
            
            double lat = source.lat;
            double lon = source.lon;
            double lat2 = destination.lat;
            double lon2 = destination.lon;


            int lx = (int) ((lon + 180.0) / 360.0 * getWidth());
            int ly = (int) ((-lat + 90.0) / 180.0 * getHeight());
            
            int lx2 = (int) ((lon2 + 180.0) / 360.0 * getWidth());
            int ly2 = (int) ((-lat2 + 90.0) / 180.0 * getHeight());

            graphics.setColor(new Color(255, 0, 0));
            graphics.drawLine(lx, ly, lx2, ly2);
        }
    
    
        public void drawAirport (Graphics graphics, Nodo airport) {

            int bubble_size = 10;
            double lat = airport.lat;
            double lon = airport.lon;

            int lx = (int) ((lon + 180.0) / 360.0 * getWidth());
            int ly = (int) ((-lat + 90.0) / 180.0 * getHeight());

            graphics.setColor(new Color(255, 0, 0));
            graphics.fillOval(lx - bubble_size / 2, ly - bubble_size / 2,
                    bubble_size, bubble_size);
            graphics.drawString(airport.code, lx + 10, ly);
        }
    }
    
