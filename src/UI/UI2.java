    private void largosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_largosActionPerformed
        String source = origen.getText();
        ArrayList<Nodo> list = this.airports.furthestAirports(source);  //Buscar los 10 aeropuertos más lejanos
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        //Buscar nodo de origen
        Nodo source_airport = this.airports.searchAirport(source);
        //Agregar nodos a la tabla
        for (Nodo airport: list) {
            float distance = this.airports.airportDistance(source_airport, airport);
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, "(" + airport.lon + ", " + airport.lat + ")", distance + " km"});
        }
        // Agregar los aeropuertos del Nodo a la Lista
        this.routes = new ArrayList<>();
        for (Nodo destination: list) {
            ArrayList<Nodo> route = this.airports.travel(source_airport.code, destination.code);
            routes.add(route);
        }
        Map.updateUI();
    }//GEN-LAST:event_largosActionPerformed

    private void caminoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_caminoActionPerformed
        String source = origen.getText();
        String destination = destino.getText();
        ArrayList<Nodo> list = this.airports.travel(source, destination);   //Buscar camino
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        Nodo prev = null;
        // Agregar aeropuertos a la tabla
        for (Nodo airport: list) {
            float distance = 0;
            if (prev != null)
                distance = prev.distance(airport);
            model.addRow(new Object[]{airport.code, airport.name, airport.city, airport.county, "(" + airport.lon + ", " + airport.lat + ")", distance + " km"});
            prev = airport;
        }
        //Agregar la ruta a la lista de rutas para graficar
        this.routes = new ArrayList<>();
        routes.add(list);
        Map.updateUI();
    }//GEN-LAST:event_caminoActionPerformed

